def build_table_rows(calls, puts, current_price, selected_columns):
    strikes = sorted(set(calls["strike"].tolist() + puts["strike"].tolist()))

    def fmt(val, fmt_str):
        try:
            return fmt_str.format(float(val))
        except Exception:
            return str(val) if val else "--"

    def build_side_cells(option_row, background, side):
        cells = []
        columns = [column for column in COLUMN_CONFIG if column["key"] in selected_columns]
        if side == "put":
            columns = list(reversed(columns))

        for idx, column in enumerate(columns):
            styles = [f"background:{background}"]
            if column["key"] in {"ask", "bid"}:
                styles.append("color:#2DD4BF" if side == "call" else "color:#FF6B4A")
            if side == "call" and idx == len(columns) - 1:
                styles.append("border-right:1px solid #2A2E39")
            if side == "put" and idx == 0:
                styles.append("border-left:1px solid #2A2E39")

            if option_row is None:
                value = "--"
            else:
                value = fmt(option_row[column["key"]], column["fmt"])
            cells.append(f'<td style="{";".join(styles)}">{value}</td>')
        return "".join(cells)

    rows_html = ""
    for strike in strikes:
        c = calls[calls["strike"] == strike].iloc[0] if strike in calls["strike"].values else None
        p = puts[puts["strike"] == strike].iloc[0] if strike in puts["strike"].values else None

        is_atm = abs(strike - current_price) == min(abs(s - current_price) for s in strikes)
        itm_call = strike < current_price
        itm_put = strike > current_price

        call_bg = "rgba(0,180,0,0.07)" if itm_call else "transparent"
        put_bg = "rgba(255,80,0,0.07)" if itm_put else "transparent"
        atm_style = (
            "border-top:1px solid rgba(45,212,191,0.5);"
            "border-bottom:1px solid rgba(45,212,191,0.5);"
            if is_atm else ""
        )
        atm_tag = '<span style="color:#2DD4BF;font-size:9px;margin-left:5px;">ATM</span>' if is_atm else ""

        call_cells = build_side_cells(c, call_bg, "call")
        put_cells = build_side_cells(p, put_bg, "put")

        rows_html += f"""
            <tr class="row" style="{atm_style}">
                {call_cells}
                <td class="strike-col">{strike:.0f}{atm_tag}</td>
                {put_cells}
            </tr>
        """

    return rows_html, len(strikes)


def format_expiration_label(expiration_value):
    exp_date = datetime.strptime(expiration_value, "%Y-%m-%d").date()
    dte = max((exp_date - date.today()).days, 0)
    return f"{exp_date.strftime('%d %b %y')} ({dte}D)"


